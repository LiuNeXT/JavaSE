package com.heima.Base.Day05;

public class Demo8_Array {
    public static void main(String[] args) {
        int[] arr = {11,22,33,55,12,18};
        print(arr);

    }

    public static void print(int[] arr){
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
